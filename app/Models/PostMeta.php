namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;

class PostMeta extends Model
{
    use HasFactory;
  
    protected $table = 'pivlu_post_meta'; // Specifies the name of the table

    protected $fillable = ['post_id', 'name', 'value'];

    public $timestamps = false; // Indicates that the model does not have timestamp fields

    // Defines the relationship between PostMeta and Post models
    public function post()
    {
        return $this->belongsTo(Post::class, 'post_id', 'id');
    }

    // Method to get a specific piece of post metadata
    public static function get_meta($post_id, $name)
    {
        $value = PostMeta::where('name', $name)->where('post_id', $post_id)->value('value');

        return $value;
    }


    // Method to add post metadata
    public static function add_meta($post_id, $name, $value)
    {
        PostMeta::updateOrInsert(
            ['name' => $name, 'post_id' => $post_id],
            ['value' => $value]
        );
    }
}
