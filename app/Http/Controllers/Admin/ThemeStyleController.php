namespace App\Http\Controllers\Admin;

use Illuminate\Http\Request;
use App\Http\Controllers\Controller;
use Illuminate\Support\Facades\Validator;
use App\Models\ThemeStyle;
use App\Models\ThemeStyleData;
use App\Models\Template;
use App\Functions\ThemeFunctions;

class ThemeStyleController extends Controller
{

    /**
     * Show all resources
     */
    public function index()
    {

        $styles = ThemeStyle::orderByDesc('is_default')->orderBy('label')->paginate(20);

        return view('admin.index', [
            'view_file' => 'admin.theme.styles.index',
            'active_menu' => 'appearance',
            'active_submenu' => 'styles',
            'styles' => $styles,
        ]);
    }


    /**
     * Create resource
     */
    public function store(Request $request)
    {

        $validator = Validator::make($request->all(), [
            'label' => 'required',
        ]);

        if ($validator->fails()) return redirect(route('admin.theme-styles.index'))->withErrors($validator)->withInput();

        if (ThemeStyle::where('label', $request->label)->exists()) return redirect(route('admin.theme-styles.index'))->with('error', 'duplicate');

        $style = ThemeStyle::create([
            'label' => $request->label,
            'is_block_style' => $request->has('is_block_style') ? 1 : 0,
        ]);

        $source_style_id = $request->source_style_id;
        if ($source_style_id) {
            $source = ThemeStyle::find($source_style_id);
            if ($source) {
                ThemeStyleData::updateOrCreate(['style_id' => $style->id], [
                    'text_size' => $source->text_size,
                    'text_color' => $source->text_color,
                    'text_font_weight' => $source->text_font_weight,
                    'text_align' => $source->text_align,
                    'link_color' => $source->link_color,
                    'link_hover_color' => $source->link_hover_color,
                    'link_underline_color' => $source->link_underline_color,
                    'link_underline_color_hover' => $source->link_underline_color_hover,
                    'link_decoration' => $source->link_decoration,
                    'link_hover_decoration' => $source->link_hover_decoration,
                    'link_font_weight' => $source->link_font_weight,
                    'link_underline_thickness' => $source->link_underline_thickness,
                    'link_underline_offset' => $source->link_underline_offset,
                    'font_family' => $source->font_family,
                    'font_family_weights' => $source->font_family_weights ?? '400',
                    'title_size' => $source->title_size,
                    'subtitle_size' => $source->subtitle_size,
                    'title_font_weight' => $source->title_font_weight,
                    'subtitle_font_weight' => $source->subtitle_font_weight,
                    'bg_color' => $source->bg_color,
                    'caption_color' => $source->caption_color,
                    'caption_size' => $source->caption_size,
                    'caption_style' => $source->caption_style,
                ]);
            }
        }

        // regenerate custom styles css file
        ThemeFunctions::generate_styles_css();

        return redirect(route('admin.theme-styles.show', ['id' => $style->id]))->with('success', 'created');
    }



    /**
     * Show resource
     */
    public function show(Request $request)
    {
        $style = ThemeStyle::find($request->id);
        if (! $style) return redirect(route('admin.theme-styles.index'));

        return view('admin.index', [
            'view_file' => 'admin.theme.styles.show',
            'active_menu' => 'appearance',
            'active_submenu' => 'styles','style' => $style,
            'fonts' => ThemeFunctions::fonts(),
            'font_sizes' => ThemeFunctions::font_sizes(),
        ]);
    }


    public function preview_style(Request $request)
    {
        $style = ThemeStyle::find($request->id);
        if (! $style) return redirect(route('admin.theme-styles.index'));

        return view('admin.preview-style', [
            'style' => $style,
        ]);
    }


    /**
     * Update style
     */
    public function update(Request $request)
    {
        $style = ThemeStyle::find($request->id);
        if (! $style) return redirect(route('admin.theme-styles.index'));

        ThemeStyleData::updateOrCreate(['style_id' => $style->id], [
            'text_size' => $request->text_size,
            'text_color' => $request->text_color,
            'text_font_weight' => $request->text_font_weight,
            'text_align' => $request->text_align,
            'link_color' => $request->link_color,
            'link_hover_color' => $request->link_hover_color,
            'link_underline_color' => $request->link_underline_color,
            'link_underline_color_hover' => $request->link_underline_color_hover,
            'link_decoration' => $request->link_decoration,
            'link_hover_decoration' => $request->link_hover_decoration,
            'link_font_weight' => $request->link_font_weight,
            'link_underline_thickness' => $request->link_underline_thickness,
            'link_underline_offset' => $request->link_underline_offset,
            'font_family' => $request->font_family,
            'font_family_weights' => $request->font_family_weights ?? '400',
            'title_size' => $request->title_size,
            'subtitle_size' => $request->subtitle_size,
            'title_font_weight' => $request->title_font_weight,
            'subtitle_font_weight' => $request->subtitle_font_weight,
            'bg_color' => $request->bg_color,
            'caption_color' => $request->caption_color,
            'caption_size' => $request->caption_size,
            'caption_style' => $request->caption_style,
        ]);


        // regenerate custom styles css file
        ThemeFunctions::generate_styles_css();

        return redirect(route('admin.theme-styles.show', ['id' => $request->id]))->with('success', 'updated');
    }


    /**
     * Show resource
     */
    public function show_custom(Request $request)
    {
        if (!check_access('developer')) return redirect(route('admin'));

        $style = TemplateStyle::find($request->id);
        if (!$style) return redirect(route('admin.template.styles'));

        return view('admin.index', [
            'view_file' => 'template.style-custom',
            'active_menu' => 'website',
            'active_submenu' => 'appearance',
            'nav_tab' => 'styles',
            'menu_section' => 'styles',
            'style' => $style,
            'fonts' => Template::template_fonts(),
            'font_sizes' => Template::template_font_sizes(),
        ]);
    }


    /**
     * Create resource
     */
    public function store_custom(Request $request)
    {
        if (!check_access('developer')) return redirect(route('admin'));

        // disable action in demo mode:
        if (config('app.demo_mode')) return redirect(route('admin'))->with('error', 'demo');

        $validator = Validator::make($request->all(), [
            'label' => 'required',
        ]);

        if ($validator->fails()) return redirect(route('admin.template.styles'))->withErrors($validator)->withInput();

        if (TemplateStyle::where('label', $request->label)->exists()) return redirect(route('admin.template.styles'))->with('error', 'duplicate');

        $style = TemplateStyle::create([
            'label' => $request->label,
            'font_family' => config('nura.defaults.font_family'),
        ]);

        $source_style_id = $request->source_style_id;
        if ($source_style_id) {
            $source = TemplateStyle::find($source_style_id);
            if ($source) {
                TemplateStyle::where('id', $style->id)->update([
                    'text_size' => $source->text_size,
                    'text_color' => $source->text_color,
                    'text_font_weight' => $source->text_font_weight,
                    'text_align' => $source->text_align,
                    'link_color' => $source->link_color,
                    'link_hover_color' => $source->link_hover_color,
                    'link_underline_color' => $source->link_underline_color,
                    'link_underline_color_hover' => $source->link_underline_color_hover,
                    'link_decoration' => $source->link_decoration,
                    'link_hover_decoration' => $source->link_hover_decoration,
                    'link_font_weight' => $source->link_font_weight,
                    'link_underline_thickness' => $source->link_underline_thickness,
                    'link_underline_offset' => $source->link_underline_offset,
                    'font_family' => $source->font_family,
                    'font_family_weights' => $source->font_family_weights ?? '400',
                    'title_size' => $source->title_size,
                    'subtitle_size' => $source->subtitle_size,
                    'title_font_weight' => $source->title_font_weight,
                    'subtitle_font_weight' => $source->subtitle_font_weight,
                    'bg_color' => $source->bg_color,
                    'caption_color' => $source->caption_color,
                    'caption_size' => $source->caption_size,
                    'caption_style' => $source->caption_style,
                ]);
            }
        }

        // regenerate css file
        Template::generate_styles_css();

        return redirect(route('admin.template.custom_styles.show', ['id' => $style->id]))->with('success', 'created');
    }


    /**
     * Update style
     */
    public function update_custom(Request $request)
    {
        if (!check_access('developer')) return redirect(route('admin'));

        // disable action in demo mode:
        if (config('app.demo_mode')) return redirect(route('admin'))->with('error', 'demo');

        $style = TemplateStyle::find($request->id);
        if (!$style) return redirect(route('admin.template.styles'));

        if (!$request->label) return redirect(route('admin.template.custom_styles.show', ['id' => $request->id]))->with('error', 'no_label');

        if (TemplateStyle::where('label', $request->label)->where('id', '!=', $request->id)->exists())
            return redirect(route('admin.template.custom_styles.show', ['id' => $request->id]))->with('error', 'duplicate');

        if ($request->use_custom_bg ?? null) $bg_color = $request->bg_color;
        else $bg_color = null;

        // font family weights
        $font_family = $request->font_family;
        $all_fonts = Template::template_fonts();
        foreach ($all_fonts as $font) {
            if ($font_family == $font->value) $font_family_weights = $font->weight;
        }

        TemplateStyle::where('id', $request->id)->update([
            'label' => $request->label,
            'text_size' => $request->text_size ?? null,
            'text_color' => $request->text_color ?? null,
            'text_font_weight' => $request->text_font_weight ?? null,
            'text_align' => $request->text_align,
            'link_color' => $request->link_color ?? null,
            'link_hover_color' => $request->link_hover_color ?? null,
            'link_underline_color' => $request->link_underline_color ?? null,
            'link_underline_color_hover' => $request->link_underline_color_hover ?? null,
            'link_decoration' => $request->link_decoration ?? null,
            'link_hover_decoration' => $request->link_hover_decoration ?? null,
            'link_font_weight' => $request->link_font_weight ?? null,
            'link_underline_thickness' => $request->link_underline_thickness ?? null,
            'link_underline_offset' => $request->link_underline_offset ?? null,
            'font_family' => $font_family ?? null,
            'font_family_weights' => $font_family_weights ?? '400',
            'bg_color' => $bg_color ?? null,
            'title_size' => $request->title_size ?? null,
            'title_font_weight' => $request->title_font_weight ?? null,
            'subtitle_font_weight' => $request->subtitle_font_weight,
            'subtitle_size' => $request->subtitle_size ?? null,
            'caption_color' => $request->caption_color,
            'caption_size' => $request->caption_size,
            'caption_style' => $request->caption_style,
        ]);

        // regenerate css file
        Template::generate_styles_css();

        return redirect(route('admin.template.custom_styles.show', ['id' => $request->id]))->with('success', 'updated');
    }


    /**
     * Remove the specified resource
     */
    public function destroy_custom(Request $request)
    {

        $style = TemplateStyle::find($request->id);
        if (!$style) return redirect(route('admin.template.styles'));

        TemplateStyle::where('id', $request->id)->delete();

        // regenerate css file
        Template::generate_styles_css();

        return redirect(route('admin.template.styles'))->with('success', 'deleted');
    }
}
