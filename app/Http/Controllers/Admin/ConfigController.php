namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use App\Models\Config;
use App\Models\ConfigLang;
use App\Models\Language;

class ConfigController extends Controller
{

    /**
     * Config settings.
     */
    public function index(Request $request)
    {
        $tab = $request->tab ?? 'plugins';

        if (!file_exists(resource_path('/views/admin/config/'.$tab.'.blade.php'))) return redirect(route('admin'));

        return view('admin.index', [
            'view_file' => 'admin.config.' . $tab,
            'active_menu' => 'config',
            'active_submenu' => 'config.website',
            'active_tab' => $tab,
            'seo_module' => $seo_module ?? null,
            'seo_configs' => $seo_configs ?? null,
            'langs' => Language::get_languages(),
        ]);
    }

    /**
     * Update config
     */
    public function update(Request $request)
    {        
        $tab = $request->tab ?? 'website';
        
        $inputs = $request->except('_token');
        
        Config::update_config($inputs);

        return redirect(route('admin.config', ['tab' => $tab]))->with('success', 'updated');
    }

}
