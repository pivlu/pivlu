namespace App\Http\Controllers\Admin;

use App\Functions\PostFunctions;
use Illuminate\Http\Request;
use App\Http\Controllers\Controller;
use App\Models\PostType;
use App\Models\PostTypeTaxonomy;
use App\Models\PostTypeTaxonomyContent;
use Illuminate\Support\Str;

class PostTypeTaxonomyController extends Controller
{

    /**
     * Display all resources
     */
    public function index(Request $request)
    {

        if (! $request->post_type_id) return redirect(route('admin.post-types.index'));

        $post_type = PostType::find($request->post_type_id);
        if (! $post_type) return redirect(route('admin.post-types.index'));
        if ($post_type->type == 'page') return redirect(route('admin.post-types.index')); // 'pages' do not have taxonomies

        $post_type_taxonomies = PostTypeTaxonomy::where('post_type_id', $request->post_type_id)->paginate(20);

        return view('admin.index', [
            'view_file' => 'admin.post-types.taxonomies',
            'active_menu' => 'website',
            'active_submenu' => 'post-types',
            'post_type' => $post_type,
            'post_type_taxonomies' => $post_type_taxonomies,
        ]);
    }


    /**
     * Display resource
     */
    public function show(Request $request)
    {
        $post_type_taxonomy_id = $request->id;

        if (! $post_type_taxonomy_id) return redirect(route('admin.post-types.index'));

        $post_type_taxonomy = PostTypeTaxonomy::find($post_type_taxonomy_id);
        if (!$post_type_taxonomy) return redirect(route('admin.post-types.index'));

        $post_type = PostType::find($post_type_taxonomy->post_type_id);
        if (! $post_type) return redirect(route('admin.post-types.index'));

        return view('admin.index', [
            'view_file' => 'admin.post-types.show-taxonomy',
            'active_menu' => 'website',
            'active_submenu' => 'post-types',
            'post_type_taxonomy' => $post_type_taxonomy,
            'post_type' => $post_type,
        ]);
    }

    /**
     * Create new resource
     */
    public function store(Request $request)
    {

        if (! $request->post_type_id) return redirect(route('admin.post-types.index'));

        $post_type = PostType::find($request->post_type_id);
        if (! $post_type) return redirect(route('admin.post-types.index'));

        $post_type_taxonomy = PostTypeTaxonomy::create([
            'post_type_id' => $request->post_type_id,
            'hierarchical' => $request->has('hierarchical') ? 1 : 0,
            'position' => $request->position ?? 1,
            'active' => $request->has('active') ? 1 : 0,
            'admin_filter' => $request->has('admin_filter') ? 1 : 0,
        ]);

        foreach (admin_languages() as $lang) {
            $name_key = 'name_' . $lang->id;
            $slug_key = 'slug_' . $lang->id;
            $label_singular_key = 'label_singular_' . $lang->id;
            $label_plural_key = 'label_plural_' . $lang->id;
            $label_create_key = 'label_create_' . $lang->id;
            $label_update_key = 'label_update_' . $lang->id;
            $label_delete_key = 'label_delete_' . $lang->id;
            $label_all_key = 'label_all_' . $lang->id;
            $label_search_key = 'label_search_' . $lang->id;

            $labels = array(
                'singular' => $request->$label_singular_key ?? $request->$name_key ?? null,
                'plural' => $request->$label_plural_key ?? $request->$name_key ?? null,
                'create' => $request->$label_create_key ?? 'Create ' . $request->$name_key,
                'update' => $request->$label_update_key ?? 'Update ' . $request->$name_key,
                'delete' => $request->$label_delete_key ?? 'Delete ' . $request->$name_key,
                'all' => $request->$label_all_key ?? 'All ' . $request->$name_key,
                'search' => $request->$label_search_key ?? 'Search ' . $request->$name_key,
            );

            $slug = $request->$slug_key ?? $request->$name_key;
            $slug = Str::slug($slug, '-');
            // Post type taxonomy slug must be unique (for same language and same post type). If slug exists, then add the post type ID in the slug
            $item_same_lang_and_slug = PostTypeTaxonomyContent::where('slug', $slug)->where('lang_id', $lang->id)->first();
            if ($item_same_lang_and_slug) {
                $existing_item_post_taxonomy_id = $item_same_lang_and_slug->post_type_taxonomy_id;
                $existing_item_post_type_id = PostTypeTaxonomy::where('id', $existing_item_post_taxonomy_id)->value('post_type_id');
                if ($existing_item_post_type_id == $post_type->id) $slug = $slug . '-' . $post_type_taxonomy->id;
            }

            PostTypeTaxonomyContent::create([
                'post_type_taxonomy_id' => $post_type_taxonomy->id,
                'lang_id' => $lang->id,
                'name' => $request->$name_key,
                'slug' => $slug,
                'labels' => json_encode($labels),
            ]);
        }

        return redirect(route('admin.post-type-taxonomies.index', ['post_type_id' => $request->post_type_id]))->with('success', 'created');
    }


    /**
     * Update resource
     */
    public function update(Request $request)
    {
        $post_type_taxonomy_id = $request->id;
        if (! $post_type_taxonomy_id) return redirect(route('admin.post-types.index'));

        $post_type_taxonomy = PostTypeTaxonomy::find($post_type_taxonomy_id);
        if (!$post_type_taxonomy) return redirect(route('admin.post-types.index'));

        $post_type = PostType::find($post_type_taxonomy->post_type_id);
        if (! $post_type) return redirect(route('admin.post-types.index'));

        PostTypeTaxonomy::where('id', $post_type_taxonomy_id)->update([
            'hierarchical' => $request->has('hierarchical') ? 1 : 0,
            'active' => $request->has('active') ? 1 : 0,
            'admin_filter' => $request->has('admin_filter') ? 1 : 0,
            'position' => $request->position ?? 1,
        ]);

        foreach (admin_languages() as $lang) {
            $original_slug = PostTypeTaxonomyContent::where(['post_type_taxonomy_id' => $request->id, 'lang_id' => $lang->id])->value('slug');

            $name_key = 'name_' . $lang->id;
            $slug_key = 'slug_' . $lang->id;
            $label_singular_key = 'label_singular_' . $lang->id;
            $label_plural_key = 'label_plural_' . $lang->id;
            $label_create_key = 'label_create_' . $lang->id;
            $label_update_key = 'label_update_' . $lang->id;
            $label_delete_key = 'label_delete_' . $lang->id;
            $label_all_key = 'label_all_' . $lang->id;
            $label_search_key = 'label_search_' . $lang->id;

            $labels = array(
                'singular' => $request->$label_singular_key ?? $request->$name_key ?? null,
                'plural' => $request->$label_plural_key ?? $request->$name_key ?? null,
                'create' => $request->$label_create_key ?? 'Create ' . $request->$name_key,
                'update' => $request->$label_update_key ?? 'Update ' . $request->$name_key,
                'delete' => $request->$label_delete_key ?? 'Delete ' . $request->$name_key,
                'all' => $request->$label_all_key ?? 'All ' . $request->$name_key,
                'search' => $request->$label_search_key ?? 'Search ' . $request->$name_key,
            );

            $slug = $request->$slug_key ?? $request->$name_key;
            $slug = Str::slug($slug, '-');
            // Post type taxonomy slug must be unique (for same language and same post type). If slug exists, then add the post type ID in the slug
            $item_same_lang_and_slug = PostTypeTaxonomyContent::where('slug', $slug)->where('lang_id', $lang->id)->where('post_type_taxonomy_id', '!=', $post_type_taxonomy_id)->first();
            if ($item_same_lang_and_slug) {
                $existing_item_post_taxonomy_id = $item_same_lang_and_slug->post_type_taxonomy_id;
                $existing_item_post_type_id = PostTypeTaxonomy::where('id', $existing_item_post_taxonomy_id)->value('post_type_id');
                if ($existing_item_post_type_id == $post_type->id) $slug = $slug . '-' . $post_type_taxonomy->id;
            }

            PostTypeTaxonomyContent::updateOrInsert(
                ['post_type_taxonomy_id' => $request->id, 'lang_id' => $lang->id],
                [
                    'name' => $request->$name_key,
                    'slug' => $slug,
                    'labels' => json_encode($labels),
                ]
            );

            if ($original_slug != $slug) PostFunctions::regenerate_post_taxonomies_url_path($post_type_taxonomy_id = $request->id);
        }

        return redirect(route('admin.post-type-taxonomies.index', ['post_type_id' => $post_type->id]))->with('success', 'updated');
    }


    /**
     * Remove the specified resource
     */
    public function destroy(Request $request)
    {
        $post_type_taxonomy_id = $request->id;
        if (! $post_type_taxonomy_id) return redirect(route('admin.post-types.index'));

        $post_type_taxonomy = PostTypeTaxonomy::find($post_type_taxonomy_id);
        if (!$post_type_taxonomy) return redirect(route('admin.post-types.index'));

        $post_type = PostType::find($post_type_taxonomy->post_type_id);
        if (! $post_type) return redirect(route('admin.post-types.index'));

        PostTypeTaxonomy::where('id', $post_type_taxonomy_id)->delete();

        return redirect(route('admin.post-type-taxonomies.index', ['post_type_id' => $post_type->id]))->with('success', 'deleted');
    }
}
