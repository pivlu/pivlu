namespace App\Http\Middleware;

use Closure;
use Illuminate\Http\Request;
use Symfony\Component\HttpFoundation\Response;
use Illuminate\Support\Facades;
use Illuminate\View\View;
use App\Models\PostType;
use Auth;

class LoggedIsStaffMiddleware
{
    /**
     * Handle an incoming request.
     *
     * @param  \Closure(\Illuminate\Http\Request): (\Symfony\Component\HttpFoundation\Response)  $next
     */
    public function handle(Request $request, Closure $next): Response
    {
        if (Auth::user() ?? null) {
            if (!(Auth::user()->role == 'admin' || Auth::user()->role == 'editor' || Auth::user()->role == 'author' || Auth::user()->role == 'contributor'))
                return redirect(route('home'));
        } else return redirect(route('home'));


        Facades\View::composer('*', function (View $view) {
            // get custom posts types (used in sidebar menu for admins and internals accounts)
            $post_types = PostType::with('default_language_content', 'plugin')->where('active', 1)->where('show_in_admin_menu', 1)->orderByDesc('core')->orderByDesc('id')->get();

            $view->with('posts_types', $post_types ?? []);
        });


        return $next($request);
    }
}
