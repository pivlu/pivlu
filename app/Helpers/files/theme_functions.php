use App\Models\Config;
use App\Models\Language;
use App\Models\Post;

if (!function_exists('theme_asset')) {
	function theme_asset($file = null)
	{
		$active_theme = Config::config()->active_theme ?? null;
		return asset('themes/' . $active_theme) . '/' . $file;
	}
}


if (!function_exists('theme_meta')) {
	function theme_meta($name = null)
	{
		if (! $name) return null;

		switch ($name) {
			case 'locale':
				return Language::get_active_language()->code;
				break;

			case 'dir':
				return Language::get_active_language()->dir;
				break;

			case 'label':
				return Config::get_config('site_label') ?? 'Pivlu website';
				break;

			case 'author':
				return Config::get_config('site_author') ?? 'Pivlu.com';
				break;

			default:
				return null;
		}
	}
}



if (!function_exists('posts')) {
	function posts($type = null)
	{
		if(!$type) $type = 'post';

		$items = Post::with('active_language_content', 'user')->where('type', $type)->where('status', 'published')->paginate(25);

		foreach($items as $item) {
			$item->title = $item->active_language_content->title;
			$item->summary = $item->active_language_content->summary;
			$item->image = image($item->media_id, 'thumb');
			$item->author_name = $item->user->name;
			$item->author_avatar = $item->user->avatar;
			$item->url = Post::generate_url($item->id, Language::get_active_language()->id);
		}
		
		//dd($items);
		return $items;
	}
}


if (!function_exists('post_image')) {
	function post_image($type = null)
	{
		if(!$type) $type = 'post';

		$items = Post::with('content')->where('type', $type)->where('status', 'published')->paginate(25);
		
		return $items;
	}
}
