namespace Pivlu\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use Pivlu\Models\Config;
use Pivlu\Models\ConfigLang;
use Pivlu\Models\Language;
use Pivlu\Models\Package;

class ConfigController extends Controller
{


    /**
     * Display the configuration page for the admin panel.
     *
     * This method handles the display of various configuration tabs in the admin area.
     * It validates the requested tab against available blade template files and falls back
     * to 'plugins' tab if the requested tab is invalid.
     *
     * @param \Illuminate\Http\Request $request The HTTP request instance containing the tab parameter
     * 
     * @return \Illuminate\View\View The rendered view for the configuration page with the following data:
     * - view_file: The blade template path for the selected tab
     * - nav_section: Current navigation section ('website')
     * - active_menu: Active menu item ('config')
     * - active_submenu: Active submenu item ('website')
     * - active_tab: The validated and active tab name
     * - langs: Available languages from the Language model
     */
    public function index(Request $request)
    {
        $tab = $request->tab ?? 'website';        
        $available_tabs = ['website', 'registration', 'languages', 'plugins'];       
        if (!in_array($tab, $available_tabs)) {
            $tab = 'plugins';
        }
        $plugins = Package::where(['has_plugin' => 1])->orderBy('name')->get();

        return view('pivlu::admin.index', [
            'view_file' => 'admin.config.' . $tab,
            'active_menu' => 'config',
            'active_submenu' => 'website',
            'active_tab' => $tab,
            'langs' => Language::get_languages(),
            'plugins' => $plugins,
        ]);
    }


    /**
     * Update configuration settings.
     *
     * This method processes the configuration update request by extracting all input
     * data except the CSRF token, passing it to the Config model for updating,
     * and redirecting back with a success message.
     *
     * @param \Illuminate\Http\Request $request The HTTP request instance containing configuration data
     * @return \Illuminate\Http\RedirectResponse Redirects back to the previous page with success message
     */
    public function update_config(Request $request)
    {
        $inputs = $request->except('_token');

        Config::update_config($inputs);

        return back()->with('success', 'updated');
    }



    /**
     * Update configuration language settings.
     *
     * This method processes form inputs where keys are formatted as "{lang_id}_{config_name}"
     * and updates the corresponding configuration values in the database.
     *
     * @param \Illuminate\Http\Request $request The HTTP request containing configuration updates
     * Expected input format: ['1_setting_name' => 'value', '2_setting_name' => 'value']
     * where the number prefix is the language ID
     * 
     * @return \Illuminate\Http\RedirectResponse Redirects back to the previous page with a success message
     */
    public function update_config_lang(Request $request)
    {
        $inputs = $request->except('_token');

        foreach ($inputs as $key => $value) {
            $array = explode('_', $key, 2);
            $lang_id = $array[0];
            $name = $array[1];
            if ($lang_id) ConfigLang::update_config($lang_id, $name, $value);
        }

        return back()->with('success', 'updated');
    }
}
