namespace Pivlu\Http\Controllers\Admin;

use Illuminate\Http\Request;
use App\Http\Controllers\Controller;
use Illuminate\Support\Facades\Validator;
use Illuminate\Support\Str;
use Pivlu\Models\User;
use Auth;

use Spatie\Permission\Models\Role;
//use Spatie\Permission\Models\Permission;

use Pivlu\Models\Permission;
use Pivlu\Models\RoleHasPermission;

class AccountRoleController extends Controller
{

    /**
     * Show all resources
     */
    public function index(Request $request)
    {
        // CHECK PERMISSION -Only admins have access to this section
        if (! Auth::user()->hasRole('admin')) return redirect(route('admin'))->with('error', 'no_permission');

        $roles = Role::withCount('users', 'permissions')->where('role_group', 'internal')->orderBy('id')->get();

        return view('pivlu::admin.index', [
            'view_file' => 'admin.accounts.roles',
            'active_menu' => 'accounts',
            'active_submenu' => 'roles',
            'roles' => $roles,
        ]);
    }


    /**
     * Show resource
     */
    public function show(Request $request)
    {
        // CHECK PERMISSION -Only admins have access to this section
        if (! Auth::user()->hasRole('admin')) return redirect(route('admin'))->with('error', 'no_permission');

        $role = Role::where('name', $request->role)->first();
        if (! $role) return redirect(route('admin.roles.index'));
        if ($role->name == 'admin') return redirect(route('admin.roles.index'));

        $all_permissions = Permission::with('post_type')->get();

        $role_permissions_ids = RoleHasPermission::with('permission_details')->where('role_id', $role->id)->pluck('permission_id')->toArray();

        return view('pivlu::admin.index', [
            'view_file' => 'admin.accounts.role',
            'active_menu' => 'accounts',
            'active_submenu' => 'roles',
            'role' => $role,
            'role_permissions_ids' => $role_permissions_ids,
            'all_permissions' => $all_permissions,
        ]);
    }


    /**
     * Create resource
     */
    public function store(Request $request)
    {
        // CHECK PERMISSION -Only admins have access to this section
        if (! Auth::user()->hasRole('admin')) return redirect(route('admin'))->with('error', 'no_permission');

        $validator = Validator::make($request->all(), [
            'label' => ['required', 'string', 'max:25'],
            'role' => ['required', 'string', 'max:25'],
        ]);

        if ($validator->fails()) return redirect(route('admin.roles.index'))->withErrors($validator)->withInput();

        $role = Str::slug($request->role, '_');

        if (Role::where('name', $role)->exists()) return redirect(route('admin.roles.index'))->with('error', 'duplicate');

        $item = Role::create([
            'name' => $role,
            'label' => $request->label,
            'description' => $request->description,
            'role_group' => 'internal',
        ]);

        // By default, add "update_profile" permission
        $perm_update_profile = Permission::where('name', 'update_profile')->first();
        if ($perm_update_profile) RoleHasPermission::create(['role_id' => $item->id, 'permission_id' => $perm_update_profile->id ?? null]);

        return redirect(route('admin.roles.show', ['role' => $role]))->with('success', 'created');
    }


    /**
     * Update resource
     */
    public function update(Request $request)
    {
        // CHECK PERMISSION -Only admins have access to this section
        if (! Auth::user()->hasRole('admin')) return redirect(route('admin'))->with('error', 'no_permission');

        $role = Role::where('name', $request->role)->first();
        if (! $role) return redirect(route('admin.roles.index'));

        $all_permissions = Permission::with('post_type')->get();

        foreach ($all_permissions as $perm) {
            if ($request->has('perm_' . $perm->id)) {
                RoleHasPermission::firstOrCreate(
                    ['role_id' => $role->id, 'permission_id' => $perm->id]
                );
            } else {
                RoleHasPermission::where(['role_id' => $role->id, 'permission_id' => $perm->id])->delete();
            }
        }

        return redirect(route('admin.roles.show', ['role' => $request->role]))->with('success', 'updated');
    }


    /**
     * Remove the specified resource
     */
    public function destroy(Request $request)
    {
        // CHECK PERMISSION -Only admins have access to this section
        if (! Auth::user()->hasRole('admin')) return redirect(route('admin'))->with('error', 'no_permission');

        $role = Role::where('name', $request->role)->first();
        if (! $role) return redirect(route('admin.roles.index'));

        $userWithRoleCount = User::with('roles')->get()->filter(
            fn($user) => $user->roles->where('name', $request->role)->toArray()
        )->count();
        if ($userWithRoleCount > 0) return redirect(route('admin.roles.index'))->with('error', 'exists_users');

        Role::find($role->id)->delete();
        RoleHasPermission::where('role_id', $role->id)->delete();

        return redirect(route('admin.roles.index'))->with('success', 'deleted');
    }
}
