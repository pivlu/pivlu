use Pivlu\Functions\FileFunctions;
use Auth;

use Spatie\Permission\Models\Role;

class AccountController extends Controller
{

    /**
     * Display a paginated listing of user accounts with filtering options.
     *
     * This method handles the accounts index page in the admin panel, allowing
     * administrators to search and filter user accounts based on various criteria.
     * Non-admin users cannot view admin accounts.
     *
     * @param \Illuminate\Http\Request $request The HTTP request instance containing:
     *     - search_terms: String to search in name, email, or username
     *     - search_blocked: Filter by blocked status ('yes'|'no')
     *     - search_email_verified: Filter by email verification status ('yes'|'no')
     *     - search_role_group: Filter by role group
     *     - search_role: Filter by specific role name
     *     - openmodal: Flag to automatically open account creation modal
     *
     * @return \Illuminate\Http\RedirectResponse|\Illuminate\View\View
     *     Returns redirect to admin dashboard with error if user lacks permission,
     *     otherwise returns the accounts index view with filtered results
     *
     * @throws \Illuminate\Auth\Access\AuthorizationException
     */
    public function index(Request $request)
    {
        // CHECK PERMISSION - view accounts
        if ($request->user()->cannot('view', User::class)) return redirect(route('admin'))->with('error', 'no_permission');

        $search_terms = $request->search_terms;
        $search_blocked = $request->search_blocked;
        $search_email_verified = $request->search_email_verified;
        $search_role_group = $request->search_role_group;
        $search_role = $request->search_role;
        $openmodal = $request->openmodal; // for automatic open modal to create account        

        $accounts = User::with('roles')->orderByDesc('id');

        if ($search_blocked == 'yes') $accounts = $accounts->whereNotNull('blocked_at');
        if ($search_blocked == 'no') $accounts = $accounts->whereNull('blocked_at');

        if ($search_email_verified == 'yes') $accounts = $accounts->whereNotNull('email_verified_at');
        if ($search_email_verified == 'no') $accounts = $accounts->whereNull('email_verified_at');
        if ($search_role) $accounts = $accounts->whereHas("roles", function ($query) use ($search_role) {
            $query->where("name", $search_role);
        });

        if ($search_role_group) $accounts = $accounts->where('role_group', $search_role_group);

        if ($search_terms) $accounts = $accounts->where(function ($query) use ($search_terms) {
            $query->where('users.name', 'like', '%' . $search_terms . '%')
                ->orWhere('users.email', 'like', '%' . $search_terms . '%')
                ->orWhere('users.username', 'like', '%' . $search_terms . '%');
        });

        // Only admins can see admin accounts
        if (!Auth::user()->hasRole('admin')) $accounts = $accounts->withoutRole('admin');

        $accounts = $accounts->paginate(20);

        return view('pivlu::admin.index', [
            'view_file' => 'admin.accounts.index',
            'active_menu' => 'accounts',
            'active_submenu' => 'accounts',
            'search_terms' => $search_terms,
            'search_blocked' => $search_blocked,
            'search_email_verified' => $search_email_verified,
            'search_role_group' => $search_role_group,
            'search_role' => $search_role,
            'accounts' => $accounts,
            'openmodal' => $openmodal,
            'all_roles' => Role::all(),
            'internal_roles_descriptions' => Role::where('role_group', 'internal')->pluck('label', 'description'),
            'internal_roles' => Role::where('role_group', 'internal')->pluck('label', 'name'),
        ]);
    }


    /**
     * Return the admin index view with provided data.
     *
     * This method renders the main administration panel view using the 'pivlu::admin.index'
     * view template and passes an array of data to be available in the view.
     *
     * @return \Illuminate\Contracts\View\View The rendered admin index view
     */
    public function show(Request $request)
    {
        // CHECK PERMISSION - view accounts
        if ($request->user()->cannot('update', User::class)) return redirect(route('admin'))->with('error', 'no_permission');

        $account = User::withCount('internal_notes')->find($request->id);
        if (!$account) return redirect(route('admin.accounts.index'));

        // Only admins can see admin accounts
        if (!Auth::user()->hasRole('admin') && $account->hasRole('admin')) return redirect(route('admin.accounts.index'));

        $block_reason = UserMeta::get_meta($account->id, 'block_reason');

        $avatar_media = $account->getFirstMedia('avatars');

        return view('pivlu::admin.index', [
            'view_file' => 'admin.accounts.show',
            'nav_section' => 'accounts',
            'active_menu' => 'accounts',
            'active_submenu' => 'accounts',
            'account' => $account,
            'block_reason' => $block_reason ?? null,
            'avatar_media' => $avatar_media,
            'menu_section' => 'details',
            'internal_roles' => Role::where('role_group', 'internal')->pluck('label', 'name'),
            'account_roles' => $account->getRoleNames()->toArray()
        ]);
    }


    /**
     * Store a newly created user account in storage.
     *
     * This method handles the creation of a new user account based on the provided
     * request data. It performs validation, permission checks, and assigns roles
     * before saving the new user to the database.
     *
     * @param \Illuminate\Http\Request $request The HTTP request containing:
     *        - name: The user's full name
     *        - email: The user's email address (must be unique)
     *        - password: The user's password (min 8 chars)
     *        - role_group: The role group ('internal' or other)
     *        - roles: Array of role names to assign (for internal role group)
     *        - email_verified_at: Checkbox to verify email
     *        - avatar: Optional avatar image file
     *        - role: Legacy role parameter for admin role check
     *
     * @return \Illuminate\Http\RedirectResponse Redirects to the accounts index with success message,
     *         or back with validation errors if creation fails.
     *
     * @throws \Illuminate\Validation\ValidationException If validation fails
     */
    public function store(Request $request)
    {
        // CHECK PERMISSION - create accounts
        if ($request->user()->cannot('create', User::class)) return redirect(route('admin'))->with('error', 'no_permission');

        // only admins can set admin role
        if ($request->role == 'admin' && !Auth::user()->hasRole('admin')) return redirect(route('admin.accounts.index'));

        $search_role = $request->search_role;

        $validator = Validator::make($request->all(), [
            'name' => ['required', 'string', 'max:255'],
            'role_group' => 'required',
            'email' => [
                'required',
                'string',
                'email',
                'max:255',
                Rule::unique(User::class),
            ],
            'password' => 'required|min:8',
        ]);

        if ($validator->fails()) return redirect(route('admin.accounts.index'))->withErrors($validator)->withInput();

        $code = HelperFunctions::generateRandomInteger(16);

        $user = User::create([
            'role_group' => $request->role_group,
            'code' => $code,
            'name' => $request->name,
            'username' => $code,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'email_verified_at' => $request->has('email_verified_at') ? now() : null,
        ]);

        // roles
        if ($request->role_group == 'internal') {
            $roles = $request->roles ?? [];
            if (count($roles) > 0) $user->assignRole($roles);
        }

        // process avatar     
        if ($request->hasFile('avatar')) FileFunctions::store_user_avatar($request->file('avatar'), $user);

        return redirect(route('admin.accounts.index', ['search_role' => $search_role]))->with('success', 'created');
    }


    /**
     * Update the specified user account.
     *
     * This method updates user account details including name, email, username, role group, 
     * blocked status, email verification, roles, avatar, and optionally the password.
     * It enforces permission checks and validation rules before updating the account.
     *
     * @param \Illuminate\Http\Request $request The HTTP request containing:
     *        - id: The user ID to update
     *        - name: The user's full name
     *        - email: The user's email address (must be unique)
     *        - username: The user's username (must be unique, 5-30 chars)
     *        - role_group: The role group ('internal' or other)
     *        - roles: Array of role names to assign (for internal role group)
     *        - blocked_at: Checkbox to block/unblock the user
     *        - block_reason: Reason for blocking (stored in user meta)
     *        - email_verified_at: Checkbox to verify email (cannot modify own)
     *        - avatar: Optional avatar image file
     *        - password: Optional new password (min 8 chars)
     *        - role: Legacy role parameter for admin role check
     *
     * @return \Illuminate\Http\RedirectResponse Redirects to the account show page with success message,
     *         or back with validation errors. Redirects to accounts index if permission denied or user not found.
     *
     * @throws \Illuminate\Validation\ValidationException If validation fails
     */
    public function update(Request $request)
    {
        // CHECK PERMISSION - update accounts
        if ($request->user()->cannot('update', User::class)) return redirect(route('admin'))->with('error', 'no_permission');

        // only admins can set admin role
        if ($request->role == 'admin' && Auth::user()->role != 'admin') return redirect(route('admin.accounts.index'));
        if ($request->role == 'admin' && !Auth::user()->hasRole('admin')) return redirect(route('admin.accounts.index'));
        $user = User::find($request->id);
        if (!$user) return redirect(route('admin.accounts.index'));

        $validator = Validator::make($request->all(), [
            'name' => 'required',
            'role_group' => 'required',
            'email' => [
                'required',
                'email',
                Rule::unique('users')->ignore($user),
            ],
            'username' => [
                'required',
                'min:5',
                'max:30',
                Rule::unique('users')->ignore($user),
            ],
        ]);

        if ($validator->fails()) return redirect(route('admin.accounts.show', ['id' => $request->id]))->withErrors($validator)->withInput();


        User::where('id', $request->id)->update([
            'role_group' => $request->role_group,
            'name' => $request->name,
            'username' => Str::slug($request->username, '.'),
            'email' => $request->email,
            'blocked_at' => $request->has('blocked_at') ? now() : null,
        ]);

        if ($request->id != Auth::user()->id) {
            if ($request->has('email_verified_at'))
                $email_verified_at = $request->has('email_verified_at')
                    ? ($user->email_verified_at ?? now())
                    : null;
        }

        // roles
        if ($request->role_group == 'internal' && $request->has('roles')) {
            // When 'roles' is present in the request, update roles accordingly.
            // An explicit empty array means: remove all existing roles.
            $roles = $request->input('roles', []);
            if (count($roles) > 0) {
                $user->assignRole($roles);
            }

            // Delete roles not sent and found in database
            $existing_roles = $user->getRoleNames();
            foreach ($existing_roles as $existing_role) {
                if (!in_array($existing_role, $roles)) {
                    $user->removeRole($existing_role);
                }
            }
        }

        // process avatar     
        if ($request->hasFile('avatar')) FileFunctions::store_user_avatar($request->file('avatar'), $user);

        if ($request->has('blocked_at')) UserMeta::add_meta($request->id, 'block_reason', $request->block_reason);

        // change password
        if ($request->password) {
            $validator = Validator::make($request->all(), [
                'password' => 'min:8',
            ]);

            if ($validator->fails()) return redirect(route('admin.accounts.show', ['id' => $request->id]))->withErrors($validator)->withInput();

            User::where('id', $request->id)->update([
                'password' => Hash::make($request->password)
            ]);
        }


        return redirect(route('admin.accounts.show', ['id' => $request->id]))->with('success', 'updated');
    }


    /**
     * Remove the specified user account from storage.
     *
     * This method handles the deletion of a user account based on the provided
     * request data. It performs permission checks before deleting the account.
     *
     * @param \Illuminate\Http\Request $request The HTTP request containing:
     *        - id: The user ID to delete
     *        - role: The role filter to maintain context after deletion
     *
     * @return \Illuminate\Http\RedirectResponse Redirects to the accounts index with success message,
     *         or back with error if deletion fails.
     *
     * @throws \Illuminate\Auth\Access\AuthorizationException
     */
    public function destroy(Request $request)
    {
        // CHECK PERMISSION - delete accounts
        if ($request->user()->cannot('delete', User::class)) return redirect(route('admin'))->with('error', 'no_permission');

        $account = User::find($request->id);
        if (!$account) return redirect(route('admin.accounts.index'));

        if (! Auth::user()->hasRole('admin') && $account->hasRole('admin')) return redirect(route('admin.accounts.index'));

        $account->delete(); // soft delete

        return redirect(route('admin.accounts.index', ['role' => $request->role]))->with('success', 'deleted');
    }
}
