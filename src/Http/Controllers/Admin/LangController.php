namespace Pivlu\Http\Controllers\Admin;

use Illuminate\Http\Request;
use App\Http\Controllers\Controller;
use Illuminate\Support\Facades\Validator;
use Pivlu\Models\ConfigLang;
use Pivlu\Functions\LocaleFunctions;
use Pivlu\Models\Language;

class LangController extends Controller
{


    /**
     * Display a listing of languages in the admin panel.
     *
     * This method handles the index page for language management,
     * typically returning a view with all available languages.
     *
     * @return \Illuminate\View\View|\Illuminate\Contracts\View\Factory
     */
    public function index()
    {
        $langs = Language::orderByDesc('is_default')->orderBy('status')->paginate(25);

        return view('pivlu::admin.index', [
            'view_file' => 'admin.config.languages',
            'nav_section' => 'website',
            'active_menu' => 'config',
            'active_submenu' => 'website',
            'active_tab' => 'langs',
            'langs' => $langs,
            'locales_array' => LocaleFunctions::locales_array(),
            'lang_codes_array' => LocaleFunctions::lang_codes_array(),
            'timezones_array' => LocaleFunctions::generate_timezone_list(),
        ]);
    }



    /**
     * Store a newly created language in the database.
     *
     * This method validates the incoming request data, checks for duplicate language names and codes,
     * handles default language logic (ensuring only one default exists and it's active),
     * creates the language record, and updates the configuration for the language.
     *
     * @param \Illuminate\Http\Request $request The HTTP request containing language data
     *  - name: The name of the language (required, max 25 chars)
     *  - code: The language code (required)
     *  - locale: The locale identifier (required)
     *  - timezone: The timezone (required)
     *  - status: The status (active|inactive|disabled)
     *  - dir: Text direction (ltr|rtl, required)
     *  - is_default: Whether this is the default language (optional)
     *  - site_label: Custom site label for this language (optional)
     *
     * @return \Illuminate\Http\RedirectResponse Redirects to languages index with success or error message
     *  - On validation failure: redirects with validation errors
     *  - On duplicate name/code: redirects with 'duplicate' error
     *  - On success: redirects with 'created' success message
     */
    public function store(Request $request)
    {

        $validator = Validator::make($request->all(), [
            'name' => 'required|max:25',
            'code' => 'required',
            'locale' => 'required',
            'timezone' => 'required',
            'status' => 'in:active,inactive,disabled',
            'dir' => 'required|in:ltr,rtl'
        ]);

        if ($validator->fails()) return redirect(route('admin.languages.index'))->withErrors($validator)->withInput();

        if (Language::where('name', $request->name)->exists()) return redirect(route('admin.languages.index'))->with('error', 'duplicate');
        if (Language::where('code', $request->code)->exists()) return redirect(route('admin.languages.index'))->with('error', 'duplicate');

        // only one language can be default
        if ($request->has('is_default')) Language::where('is_default', 1)->update(['is_default' => 0]);

        // default language must be active
        if ($request->has('is_default')) $status = 'active';
        else $status = $request->status;

        $lang = Language::create([
            'name' => $request->name,
            'code' => $request->code,
            'locale' => $request->locale,
            'is_default' => $request->has('is_default') ? 1 : 0,
            'status' => $status,
            'timezone' => $request->timezone ?? 'Europe/London',
            'dir' => $request->dir ?? 'ltr',
            'site_label' => $request->site_label,
        ]);

        ConfigLang::update_config($lang->id, 'site_label', $request->site_label ?? config('app.name'));

        return redirect(route('admin.languages.index'))->with('success', 'created');
    }


    /**
     * Update the specified language in the database.
     *
     * This method validates the incoming request data, checks for duplicate language names and codes,
     * handles default language logic (ensuring only one default exists and it's active),
     * updates the language record, and updates the configuration for the language.
     *
     * @param \Illuminate\Http\Request $request The HTTP request containing language data
     *  - id: The ID of the language to update (required)
     *  - name: The name of the language (required, max 25 chars)
     *  - code: The language code (required)
     *  - locale: The locale identifier (required)
     *  - timezone: The timezone (required)
     *  - status: The status (active|inactive|disabled)
     *  - dir: Text direction (ltr|rtl, required)
     *  - is_default: Whether this is the default language (optional)
     *  - site_label: Custom site label for this language (optional)
     *
     * @return \Illuminate\Http\RedirectResponse Redirects to languages index with success or error message
     *  - On validation failure: redirects with validation errors
     *  - On duplicate name/code: redirects with 'duplicate' error
     *  - On success: redirects with 'updated' success message
     */
    public function update(Request $request)
    {

        $lang = Language::find($request->id);
        if (!$lang) return redirect(route('admin.languages.index'));

        $validator = Validator::make($request->all(), [
            'name' => 'required|max:25',
            'code' => 'required',
            'locale' => 'required',
            'timezone' => 'required',
            'dir' => 'required|in:ltr,rtl',
            'status' => 'in:active,inactive,disabled',
        ]);

        if ($validator->fails()) return redirect(route('admin.languages.index'))->withErrors($validator)->withInput();

        if (Language::where('name', $request->name)->where('id', '!=', $request->id)->exists()) return redirect(route('admin.languages.index'))->with('error', 'duplicate');
        if (Language::where('code', $request->code)->where('id', '!=', $request->id)->exists()) return redirect(route('admin.languages.index'))->with('error', 'duplicate');

        // only one language can be default
        if ($request->has('is_default')) Language::where('is_default', 1)->update(['is_default' => 0]);

        // default language must be active
        if ($request->has('is_default')) $status = 'active';
        else $status = $request->status;

        Language::where('id', $request->id)->update([
            'name' => $request->name,
            'code' => $request->code,
            'locale' => $request->locale,
            'is_default' => $request->has('is_default') ? 1 : 0,
            'status' => $status,
            'timezone' => $request->timezone ?? 'Europe/London',
            'dir' => $request->dir ?? 'ltr',
            'site_label' => $request->site_label,
        ]);

        ConfigLang::update_config($lang->id, 'site_label', $request->site_label ?? config('app.name'));

        return redirect(route('admin.languages.index'))->with('success', 'updated');
    }


    /**
     * Remove the specified language from the database.
     *
     * This method checks if the language exists and is not the default language,
     * then deletes the language record from the database.
     *
     * @param \Illuminate\Http\Request $request The HTTP request containing the language ID
     *  - id: The ID of the language to delete (required)
     *
     * @return \Illuminate\Http\RedirectResponse Redirects to languages index with success or error message
     *  - If language not found: redirects to languages index
     *  - If trying to delete default language: redirects with 'default' error
     *  - On success: redirects with 'deleted' success message
     */
    public function destroy(Request $request)
    {

        $lang = Language::find($request->id);
        if (!$lang) return redirect(route('admin.languages.index'));

        if (Language::where('id', $request->id)->value('is_default') == 1) return redirect(route('admin.languages.index'))->with('error', 'default');

        Language::where('id', $request->id)->delete();

        return redirect(route('admin.languages.index'))->with('success', 'deleted');
    }
}
