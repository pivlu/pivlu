namespace Pivlu\Http\Controllers\Admin;

use Illuminate\Http\Request;
use App\Http\Controllers\Controller;
use Pivlu\Models\Package;

class PackageController extends Controller
{

    public function index(Request $request)
    {
        $plugins = Package::where(['has_plugin' => 1])->orderBy('name')->get();

        return view('pivlu::admin.index', [
            'view_file' => 'admin.config.packages',
            'active_menu' => 'config',
            'active_submenu' => 'packages',
            'plugins' => $plugins,
        ]);
    }


    public function update(Request $request)
    {
        $modules = $request->except('_token');
        foreach ($modules as $module => $status) {
            $module_array = explode('_', $module);
            if ($module_array[0] == 'module') {
                $module_id = $module_array[1];

                $module = Package::where('id', $module_id)->first();

                if ($status == 'active' || $status == 'inactive' || $status == 'disabled')
                    $module->update(['status' => $status]);
                
            }
        }

        return redirect(route('admin.packages'))->with('success', 'updated');
    }
}
