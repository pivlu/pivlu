namespace Pivlu\Cms\Http\Controllers\Web;

use App\Http\Controllers\Controller;
use Illuminate\Http\Request;
use Pivlu\Cms\Models\Config;

class ToolsController extends Controller
{

    /**
     * Maintenance mode 
     */
    public function maintenance(Request $request)
    {
        if (!(Config::get_config('website_maintenance_enabled') ?? null)) return redirect(route('home'));

        return view('pivlu::web.maintenance', []);
    }


    /**
     * User blocked
     */
    public function user_blocked(Request $request)
    {
        if (Auth::user()->blocked_at) {
            $block_reason = UserMeta::get_meta(Auth::user()->id, 'block_reason');
        }

        return view('pivlu::web.user-blocked', ['block_reason' => $block_reason]);
    }



    /**
     * Theme fake post
     */
    public function theme_fake_post(Request $request)
    {
        $active_theme = Config::config()->active_theme ?? null;
        $sample_file = $request->file;

        return view('pivlu::themes.' . $active_theme . '.includes.samples.' . $sample_file, []);
    }
}
