namespace Pivlu\Console\Commands;

use Illuminate\Console\Command;
use Artisan;
use Illuminate\Support\Facades\Hash;
use Illuminate\Support\Facades\Validator;
use Illuminate\Support\Facades\Schema;
use Pivlu\Models\Config;
use Pivlu\Models\User;
use Pivlu\Functions\HelperFunctions;
use Pivlu\Functions\SetupFunctions;

class InstallCommand extends Command
{

    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'pivlu:install';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Allows to install the app directly through CLI';

    /**
     * Execute the console command.
     *
     * @return mixed
     */
    public function handle()
    {
        if (Schema::hasTable('pivlu_config')) {
            if (Config::get_config('installed_at') ?? null) {
                $this->error('Pivlu is already installed!');
                $this->line('You can update Pivlu using "pivlu:update" command.');
                return;
            }
        }

        // Package artisan
        Artisan::call('vendor:publish --tag=config');
        Artisan::call('vendor:publish --tag=assets');
        Artisan::call('vendor:publish --tag=migrations');

        // Database tables
        $this->line('Setup database tables');
        Artisan::call('migrate');
        $this->info('Database tables created');

        $this->line('Setup default language');
        SetupFunctions::check_default_language();
        
        $this->line('Setup website default post types');
        SetupFunctions::check_default_post_types();

        $this->line('Check default roles and permissions');
        SetupFunctions::check_default_roles_and_permissions();
    
        // Add admin account
        $this->line('Adding administrator account');
        $admin_name = $this->askValid('Input administrator full name: ', 'admin_name', ['required', 'min:3']);
        $admin_email = $this->askValid('Input administrator email: ', 'admin_email', ['required', 'email']);
        $admin_pass = $this->askValid('Input administrator password: ', 'admin_pass', ['required', 'min:5']);
        $admin_code = HelperFunctions::generateRandomInteger(16);
        User::updateOrInsert(['email' => $admin_email], [
            'name' => $admin_name ?? 'Admin',
            'email' => $admin_email,
            'code' => $admin_code,
            'role_group' => 'admin',
            'username' => $admin_code,
            'password' => Hash::make($admin_pass),
            'email_verified_at' => now(),
            'created_at' => now(),
        ]);

        $admin_user = User::where('email', $admin_email)->first();                
        $admin_user->assignRole('admin');
                
        $this->line('Check default block types');
        SetupFunctions::check_default_block_types();

        $this->line('Check default button');
        SetupFunctions::check_default_button();

        $this->line('Check default style');
        SetupFunctions::check_default_style();

        $this->line('Check default menu');
        SetupFunctions::check_default_menu();

        $this->line('Check default theme');
        SetupFunctions::check_default_theme();
        
        $this->line('Check website default form and statuses');
        SetupFunctions::check_form();
        
        $this->line('Check website settings');
        SetupFunctions::check_default_website_settings();

        // Create the symbolic link 
        Artisan::call('storage:link');

        // Website is in maintenance mode by default, ONLY for first install
        if (! Config::get_config('website_maintenance_enabled')) {
            Config::update_config('website_maintenance_enabled', 'on');
            Config::update_config('website_maintenance_text', '<h1>Website under construction</h1>
            Powered by <a href="https://pivlu.com">Pivlu</a> - Free and Open Source CMS and Website Builder
            <hr>
            <small class="text-muted">If you are the owner of this website, go to administrator area and start building your awesome website.</small>');
        }

        Config::update_config('installed_at', now());
        $this->info('The install was successful!');
    }


    protected function askValid($question, $field, $rules)
    {
        $value = $this->ask($question);

        if ($message = $this->validateInput($rules, $field, $value)) {
            $this->error($message);

            return $this->askValid($question, $field, $rules);
        }

        return $value;
    }


    protected function validateInput($rules, $fieldName, $value)
    {
        $validator = Validator::make([
            $fieldName => $value
        ], [
            $fieldName => $rules
        ]);

        return $validator->fails()
            ? $validator->errors()->first($fieldName)
            : null;
    }
}
