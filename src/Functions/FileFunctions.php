namespace Pivlu\Functions;

class FileFunctions
{

    /**
     * Upload file to the Storage
     * 
     * @return object|null The object of uploaded file or null if the upload fails
     */

    public static function store_file($model, $requestFile, $mediaCollection)
    {

        if (!$model) return false;
        if (!$requestFile) return false;
        if (!$mediaCollection) return false;

        $item = $model->addMedia($requestFile)->toMediaCollection($mediaCollection);

        return $item;
    }



    /**
     * Upload an avatar image to the server. If the upload is successful, add avatar to user.
     * 
     * @return true|false 
     */
    public static function store_user_avatar($requestFile, $user)
    {
        if (!$requestFile) return false;
        if (!$user) return false;

        $media_item = $user->addMedia($requestFile)->toMediaCollection('avatars');

        $user->update(['avatar_media_id' => $media_item->id]);

        return true;
    }


    /**
     * Delete file from the Storage
     * 
     * @return bool True if the file was deleted, false otherwise
     */
    public static function delete_file($model, $mediaCollection = null)
    {
        if (! $model) return false;        
        $mediaItem = $model->getFirstMedia($mediaCollection);
        if( ! $mediaItem) return false;
        $mediaItem->delete();

        return true;
    }

    /**
     * Delete all files from a specific media collection
     * 
     * @return bool True if the files were deleted, false otherwise
     */
    public static function delete_files($model, $mediaCollection)
    {
        if (! $model) return false;

        $mediaItems = $model->getMedia($mediaCollection);
        foreach ($mediaItems as $mediaItem)
            $mediaItem->delete();

        return true;
    }
}
