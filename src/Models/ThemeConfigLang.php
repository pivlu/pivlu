namespace Pivlu\Cms\Models;

use Illuminate\Database\Eloquent\Model;

class ThemeConfigLang extends Model
{
    protected $table = 'pivlu_theme_config_lang';

    protected $fillable = ['theme_id', 'lang_id', 'name', 'value'];


    /**
     * Get theme configs from database
     *
     * @return object
     */
    public static function config($theme_id = null, $lang_id = null)
    {
        if (!$theme_id ?? null) $theme_id = Theme::where('is_active', 1)->value('id');
        if (!$lang_id ?? null) $lang_id = Language::where('is_default', 1)->value('id');
        $results = ThemeConfigLang::where(['theme_id' => $theme_id, 'lang_id' => $lang_id])->pluck('value', 'name')->toArray();

        return (object) $results;
    }


    /**
     * Get specific config value from database
     *
     * @return string
     */
    public static function get_config($theme_id, $lang_id, $name)
    {
        return ThemeConfigLang::where(['theme_id' => $theme_id, 'lang_id' => $lang_id, 'name' => $name])->value('value') ?? null;
    }


    /**
     * Update config
     *
     * @return null
     */
    public static function update_config($theme_id, $lang_id, $name, $value = null)
    {
        if (is_array($name)) {
            foreach ($name as $key => $value) {
                if ($key != '_token') ThemeConfigLang::updateOrCreate(['theme_id' => $theme_id, 'lang_id' => $lang_id, 'name' => $key], ['value' => $value]);
            }
        } else {
            ThemeConfigLang::updateOrCreate(['theme_id' => $theme_id, 'lang_id' => $lang_id, 'name' => $name], ['value' => $value]);
        }

        return null;
    }
}
