namespace Pivlu\Models;

use Illuminate\Database\Eloquent\Model;
use Spatie\MediaLibrary\HasMedia;
use Spatie\MediaLibrary\InteractsWithMedia;
use Spatie\MediaLibrary\MediaCollections\Models\Media;
use Spatie\Image\Enums\Fit;

class BlockItemContent extends Model implements HasMedia
{
    use InteractsWithMedia;

    protected $fillable = [
        'block_item_id',
        'block_id',
        'lang_id',
        'media_id',
        'data',
        'header',
    ];

    protected $table = 'pivlu_block_item_content';

    /**
     * Indicates if the model should be timestamped.
     *
     * @var bool
     */
    public $timestamps = false;

    /**
     * Register the media collections.
     *
     * @return void
     */
    public function registerMediaCollections(): void
    {
        // IMPORTANT: DO NOT ADD singleFile(): we need to allow multiple files for different languages
        $this
            ->addMediaCollection('block_item_media')
            ->withResponsiveImages()
            ->useFallbackUrl(asset('assets/img/no-image.png'))
            ->useFallbackUrl(asset('assets/img/no-image-thumb.png'), 'thumb')
            ->useFallbackPath(public_path('/assets/img/no-image.png'))
            ->useFallbackPath(public_path('/assets/img/no-image-thumb.png'), 'thumb');
    }

    /**
     * Register the media conversions.
     *
     * @param  \Spatie\MediaLibrary\MediaCollections\Models\Media|null  $media
     * @return void
     */
    public function registerMediaConversions(Media $media = null): void
    {
        $this->addMediaConversion('large')
            ->fit(Fit::Contain, 1200, 800)
            //->keepOriginalImageFormat()
            ->format('webp')
            ->quality(80)
            ->optimize()
            ->nonQueued();

        $this->addMediaConversion('small')
            ->fit(Fit::Contain, 600, 400)
            //->keepOriginalImageFormat()
            ->format('webp')
            ->nonQueued();

        $this->addMediaConversion('square')
            ->fit(Fit::Fill, 1000, 1000)
            //->keepOriginalImageFormat()
            ->format('webp')
            ->nonQueued();

        $this->addMediaConversion('small_square')
            //->keepOriginalImageFormat()
            ->format('webp')
            ->fit(Fit::FillMax, 350, 350)
            ->nonQueued();

        $this->addMediaConversion('thumb_square')
            ->format('webp')
            //->keepOriginalImageFormat()
            ->fit(Fit::FillMax, 150, 150)
            ->nonQueued();

        $this->addMediaConversion('thumb')
            ->format('webp')
            //->keepOriginalImageFormat()
            ->fit(Fit::Max, 150, 150)
            ->nonQueued();

        $this->addMediaConversion('crop')
            ->format('webp')
            //->keepOriginalImageFormat()
            ->fit(Fit::Crop, 350, 350)
            ->nonQueued();
    }
}
