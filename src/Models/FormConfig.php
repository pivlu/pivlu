namespace Pivlu\Models;

use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\Relations\BelongsTo;

class FormConfig extends Model
{
    protected $fillable = [
        'form_id',
        'name',
        'value',
    ];

    protected $table = 'pivlu_form_config';

    public $timestamps = false;

    public function form(): BelongsTo
    {
        return $this->BelongsTo(Form::class, 'form_id');
    }    


    /**
     * Get specific config value from database
     *
     * @return string
     */
    public static function get_config($id, $name)
    {
        return FormConfig::where(['form_id' => $id, 'name' => $name])->value('value') ?? null;
    }

    /**
     * Update config
     *
     * @return null
     */
    public static function update_config($id, $name, $value = null)
    {
        if (is_array($name)) {
            foreach ($name as $key => $value) {
                if ($key != '_token') FormConfig::updateOrCreate(['form_id' => $id, 'name' => $key], ['value' => $value]);
            }
        } else {
            FormConfig::updateOrCreate(['form_id' => $id, 'name' => $name], ['value' => $value]);
        }

        return null;
    }
}
