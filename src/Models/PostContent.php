namespace Pivlu\Cms\Models;

use Illuminate\Database\Eloquent\Model;


class PostContent extends Model
{

    protected $fillable = [
        'post_id',
        'lang_id',
        'title',
        'summary',
        'slug',
        'url',
        'meta_title',
        'meta_description',
        'search_terms',
    ];

    protected $table = 'pivlu_post_content';

    public function post()
    {
        return $this->belongsTo(Post::class, 'post_id');
    }
    
}
