use Pivlu\Models\Form;
use Pivlu\Models\FormField;
use Pivlu\Models\FormFieldContent;
use Pivlu\Models\Language;

// get form fields (for active language)
if (!function_exists('block_form_fields')) {
	function block_form_fields($id)
	{
		// get form fields
		$fields = FormField::where(['form_id' => $id, 'active' => 1])->orderBy('position')->get();

		$data = [];

		foreach ($fields as $field) {

			$field_lang = FormFieldContent::where('field_id', $field->id)->where('lang_id', Language::get_active_language()->id)->first();
			$field_default_lang = FormFieldContent::where('field_id', $field->id)->where('lang_id', Language::get_default_language()->id)->first();

			$data[] = array(
				'id' => $field->id,
				'type' => $field->type,
				'required' => $field->required,
				'col_md' => $field->col_md,
				'label' => $field_lang->label ?? $field_default_lang->label ?? null,
				'info' => $field_lang->info ?? $field_default_lang->info ?? null,
				'dropdowns' => $field_lang->dropdowns ?? $field_default_lang->dropdowns ?? null
			);
		}

		return (json_decode(json_encode($data)));
	}
}


// check if a form ave reCAPTCHA enabled
if (!function_exists('check_form_have_recaptcha')) {
	function check_form_have_recaptcha($form_id)
	{
		if (!$form_id)
			return false;

		$form = Form::find($form_id);
		if (!$form)
			return false;

		if (($form->recaptcha ?? null) == 1)
			return true;
		else
			return false;
	}
}
